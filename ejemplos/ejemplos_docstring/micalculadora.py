def sumar(sumando1 : int, sumando2 : int) -> int:
    """
    Suma sumando1 y sumando2 y retorna el resultado de dicha suma.
    """
    resultado = sumando1+sumando2
    return resultado

def restar(operando1 : int, operando2 : int) -> int:
    """
    Resta operando2 a operando1 y retorna el resultado.
    """
    resultado = operando1-operando2
    return resultado